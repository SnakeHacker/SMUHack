# 生成字典

i = 100000
diclist = []
while (i < 1000000):
    if (i == 999999):
        diclist.append(str(i))
    else:
        diclist.append(str(i) + '\n')
    i += 1

f = file("dic.txt", 'w')
f.writelines(diclist)
f.close()
